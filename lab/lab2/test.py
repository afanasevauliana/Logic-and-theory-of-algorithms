def count_ways(N, x, y):
    """
    Возвращает количество способов дойти из (0,0) в (x,y) ровно за N шагов.
    """
    # Базовый случай: шагов не осталось
    if N == 0:
        return 1 if (x == 0 and y == 0) else 0
    
    # Рекурсивный случай: делаем шаг в одном из 4 направлений
    return (count_ways(N - 1, x, y - 1) +  # Север (y увеличивается)
            count_ways(N - 1, x, y + 1) +  # Юг   (y уменьшается)
            count_ways(N - 1, x - 1, y) +  # Восток (x увеличивается)
            count_ways(N - 1, x + 1, y))   # Запад  (x уменьшается)


# ========== ПРОСТАЯ ПРОВЕРКА ==========
if __name__ == "__main__":
    # Проверяем наш ручной пример: N=2, цель (0,0)
    result = count_ways(2, 1, 1)
    print(f"Количество способов за 2 шага вернуться в (0,0): {result}")
    # Ожидаем 4
    
    # Ещё пример: N=1, цель (1,0) — только один шаг на Восток
    result2 = count_ways(1, 1, 0)
    print(f"Количество способов за 1 шаг попасть в (1,0): {result2}")